    private void buttonChangePWActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonChangePWActionPerformed

        if (Validering.textFaltHarVarde(fieldOldPW) && Validering.textFaltHarVarde(fieldNyttPW) && Validering.textFaltHarVarde(fieldNyttPWUpprepa)){
            String userOldPW = fieldOldPW.getText();
            String userNyttPW = fieldNyttPW.getText();
            String userNyttPWUpprepa = fieldNyttPWUpprepa.getText();
            
            String userID = Inlogg.getUserID(); 
            System.out.println(userID);
            String realOldPW = Inlogg.getUserLosen();
            
            if (userOldPW.equals(realOldPW)){
                if (userNyttPW.equals(userNyttPWUpprepa)){              

                    try {
                      
                        if (!Inlogg.getVal()){
                            idb.fetchSingle("UPDATE alien SET Losenord = '" + userNyttPW + "' WHERE Alien_ID = " + userID);
                            JOptionPane.showMessageDialog(null, "Password changed.");
                            new Startsida_Alien(idb).setVisible(true);
                           
                        }

                        else if (Inlogg.getVal()){
                            idb.fetchSingle("UPDATE agent SET Losenord = '" + userNyttPW + "' WHERE Agent_ID = " + userID);
                            new Startsida_Agent().setVisible(true);
                        }
                        else 
                            System.out.println("something went wrong");
                    }
                    catch (Exception ettUndantag){
                        JOptionPane.showMessageDialog(null, "Something went wrong. Please contact your IT-Administrator.");
                        System.out.println("InternFelmeddelande:" + ettUndantag.getMessage());
                    }
                }
                else 
                    JOptionPane.showMessageDialog(null, "The two matching passwords does not match.");
            }
            else 
                JOptionPane.showMessageDialog(null, "Your entered password does not match your old password.");
            
        }
    }//GEN-LAST:event_buttonChangePWActionPerformed

